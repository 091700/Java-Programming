public class StringConcat {
    public static void main(String[] args) {
        String message = "Welcome to Java";
        String s1 = "Chapter2";
        String s2 = "s2";
        String s3 = "s3";
        System.out.println(message + s1 + s2);
        System.out.println(s1 + s2);
        System.out.println(s1 + s3);
    }
}
